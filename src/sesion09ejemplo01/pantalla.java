    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_SPACE
                || evt.getKeyCode() == KeyEvent.VK_W) {
            if (balitaExiste == true) {

            } else {
                posYbalita = 380;
                balita.setBounds(tanque.getX() + 40, (int) posYbalita,
                        10, 20);
                balitaExiste = true;
                balita.setVisible(true);
            }

        }

        //if(evt.getKeyCode() == KeyEvent.VK_DOWN
        //        ||evt.getKeyCode() == KeyEvent.VK_S){
        //    tanque.setBounds(tanque.getX(), tanque.getY() + 5,
        //tanque.getWidth(), tanque.getHeight());
        //}
        if (evt.getKeyCode() == KeyEvent.VK_RIGHT
                || evt.getKeyCode() == KeyEvent.VK_D) {
            tanque.setBounds(tanque.getX() + 5, tanque.getY(),
                    tanque.getWidth(), tanque.getHeight());
        }

        if (evt.getKeyCode() == KeyEvent.VK_LEFT
                || evt.getKeyCode() == KeyEvent.VK_A) {
            tanque.setBounds(tanque.getX() - 5, tanque.getY(),
                    tanque.getWidth(), tanque.getHeight());
        }

    }//GEN-LAST:event_formKeyPressed

