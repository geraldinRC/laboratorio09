    public pantalla() {

        initComponents();
        balita.setVisible(false);

        Timer timer = new Timer(95, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {

                if (posx >= 410) {
                    dir = -1;
                }
                if (posx <= 0) {
                    dir = 1;
                }

                posx = posx + 5 * dir;

                posYbalita = posYbalita - 5;
                enemigo.setBounds((int) posx, enemigo.getY(),
                        enemigo.getWidth(),
                        enemigo.getHeight());

                if (balitaExiste) {
                    balita.setBounds(balita.getX(), (int) posYbalita,
                            balita.getWidth(),
                            balita.getHeight());
                    System.out.println(balita.getY());
                }
                if (posYbalita <= 10) {
                    //balita.setVisible(false);
                    balitaExiste = false;
                    posYbalita = 0;
                }
                if ((balita.getY() <= enemigo.getY() + 50 && balita.getY() > 20)
                        && (balita.getX() >= enemigo.getX() - 80)
                        && (balita.getX() <= enemigo.getX() + 80)) {
                    balitaExiste = false;
                    enemigo.setVisible(false);
                    balita.setVisible(false);

                    // Mensaje
                    JOptionPane.showMessageDialog(enemigo, 
                            "Â¡Ganaste la partida!");

                }
            }
        });

        // Inicia el temporizador
        timer.start();
    }

